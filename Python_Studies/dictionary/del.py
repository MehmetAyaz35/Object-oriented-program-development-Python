my_list1 = [1, 2, 3, 4, 5]
del my_list1[0:3]  #deletes elements index 0, 1, 2
print(my_list1)

my_list2 = [1, 2, 3, 4, 5]
del my_list2[2]  # deletes the element at index 2
print(my_list2)


my_dict = {'a': 1, 'b': 2, 'c': 3}
del my_dict['b']  # Deletes the key-value pair with key 'b'
print(my_dict)
# Now, my_dict is {'a': 1, 'c': 3}


