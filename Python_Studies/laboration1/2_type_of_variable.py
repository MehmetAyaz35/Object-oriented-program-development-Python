# Skapa några olika variabler med olika typer
integer_variable = 42
float_variable = 3.14
string_variable = "Hello, World!"
list_variable = [1, 2, 3]
dictionary_variable = {"key": "value"}
boolean_variable = True



# Använd type() för att kontrollera variablernas typer och skriv ut resultaten
print("integer_variable är av typen:", type(integer_variable))
print("float_variable är av typen:", type(float_variable))
print("string_variable är av typen:", type(string_variable))
print("list_variable är av typen:", type(list_variable))
print("dictionary_variable är av typen:", type(dictionary_variable))
print("boolean_variable är av typen:", type(boolean_variable))
